<?php

// untuk membandingkan dua jenis, lalu bisa di selected
function getSelected($s1, $s2)
{
    if($s1 == $s2)
    {
        echo "selected";
    }
}

// untuk membandingkan dua jenis, lalu bisa di checked
function getChecked($s1, $s2)
{
    if($s1 == $s2)
    {
        echo "checked";
    }
}

?>

